/**
 * Given a monthly FAAC inflow amount, distribute it across codes using the
 * percentage-allocated table. Returns a map of { code → amount }.
 */
/**
 * Distribute a monthly FAAC inflow across all 55 codes using the
 * % Allocated table. Returns { code → kobo-rounded amount }. Any rounding
 * remainder vs. the input total is folded into the single largest
 * allocation so the sum always equals `monthlyInflow` to the kobo.
 */
export function distributeMonthlyInflow2026(monthlyInflow: number): Record<string, number> {
  const out: Record<string, number> = {};
  let running = 0;
  let largestCode = "";
  let largestPct = -Infinity;
  for (const [code, pct] of Object.entries(PERCENT_ALLOCATED_2026)) {
    const v = Math.round(monthlyInflow * (pct / 100) * 100) / 100;
    out[code] = v;
    running += v;
    if (pct > largestPct) { largestPct = pct; largestCode = code; }
  }
  const diff = Math.round((monthlyInflow - running) * 100) / 100;
  if (diff !== 0 && largestCode) {
    out[largestCode] = Math.round((out[largestCode] + diff) * 100) / 100;
  }
  return out;
}